# 字典中存储的数据最特别的地方在于它们始终以键值对的方式存储

# 定义字典
my_dict1 = {"王力宏": 99, "周杰伦": 88, "林俊杰": 77}

# 定义空字典
# 定义空集合时必须通过set()的原因是{}的方式被定义空字典占用了
