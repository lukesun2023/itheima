# 类的组成:
# 1.类的属性(定义在类内部的变量,称为成员变量)
# 2.类的行为(定义在类内部的函数,称为成员方法)
# 类和成员方法的定义语法:
# class 类名称:
#   成员变量
#   def 成员方法(self):