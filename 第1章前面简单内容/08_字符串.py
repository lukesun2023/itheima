# 字符串的多种定义形式:
# 1.单引号定义法:name='黑马程序员'
# 2.双引号定义法:name="黑马程序员"
# 3.三引号定义法:name="""黑马程序员"""

# 单引号定义法:
name = '黑马程序员'
print(type(name))

# 双引号定义法:
name = "黑马程序员"
print(type(name))

# 三引号定义法:
name = """
我
是
黑马~
程序员!
"""
print(type(name))

# 字符串中输出'或"的方法:
# 1.单引号和双引号交叉使用,想输出单引号,字符串两侧用双引号,想输出双引号,字符串两侧用单引号
name = '黑马"程序员"'
print(name)
name = "黑马'程序员'"
print(name)
# 使用转义字符的方法(使用\解除引号的作用):
name = "\"黑马程序员\""
print(name)
name = '\'黑马程序员\''
print(name)
